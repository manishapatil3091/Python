# Explain how python manages memory automatically.
# why does the programmer not need to explicitely allocate or free mamory.


#ANSWER---

# Python manages memory automatically using Garbage colletion , hence progarammer need not to explicitely.
# When an object is created, Python allocates the required memory and returns a reference to the object. 
# When an object is no longer referenced, its memory is automatically reclaimed.
# If nothing refers variable , the memory allocated for it may later colleted by garbage collecter.